from __future__ import annotations

import getpass
import os
from dataclasses import dataclass
from pathlib import Path

from mira.config.paths import paths


# First-run wizard. Walks through the four required keys (plus a few opt-in
# ones), writes them to `<config_dir>/.env`, and refuses to overwrite an
# existing non-empty value unless the user explicitly asks to replace it.
#
# Why terminal, not a GUI:
#   * The menu-bar app is an LSUIElement — it has no main window to host a
#     setup screen, and building a one-shot rumps window just for setup is
#     more surface than it's worth.
#   * macOS mic/accessibility permissions are granted via system dialogs we
#     can't replicate anyway; users end up in System Settings either way.
#   * `mira setup` from Terminal is a stable invocation target we can
#     document and re-run if something breaks.


@dataclass(frozen=True)
class KeyPrompt:
    env_name: str
    label: str
    help_url: str
    required: bool
    # Whether to use getpass (hide keystrokes). Voice IDs and usernames are
    # not secret; API keys are.
    secret: bool = True


