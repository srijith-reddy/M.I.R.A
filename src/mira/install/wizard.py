def _parse_env_file(path: Path) -> dict[str, str]:
    """Very small `.env` parser — no quoting rules, no interpolation. Good
    enough for the fields we own and keeps us out of dotenv-write land."""
    if not path.exists():
        return {}
    out: dict[str, str] = {}
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        k, _, v = line.partition("=")
        out[k.strip()] = v.strip().strip('"').strip("'")
    return out


def _write_env_file(path: Path, values: dict[str, str]) -> None:
    """Atomic write so an interrupted wizard can't leave a half-written .env
    that breaks boot. Also chmod 600 since the file holds secrets."""
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(path.suffix + ".tmp")
    lines = ["# Generated by `mira setup`. Edit by hand if you prefer."]
    for k in sorted(values):
        lines.append(f'{k}="{values[k]}"')
    tmp.write_text("\n".join(lines) + "\n")
    os.chmod(tmp, 0o600)
    tmp.replace(path)


def _prompt_one(p: KeyPrompt, current: str | None) -> str | None:
    """Returns the new value (possibly unchanged), or None to skip.

    Empty input keeps the current value; 'clear' wipes it; anything else
    replaces. Optional keys can be left blank forever."""
    shown_current = "(set)" if current else "(not set)"
    lead = f"\n{p.label}"
    if p.help_url:
        lead += f"  →  {p.help_url}"
    print(lead)
    print(f"  env: {p.env_name}   current: {shown_current}")

    prompt = "  value (blank = keep, 'clear' = remove): "
    raw = getpass.getpass(prompt) if p.secret else input(prompt)
    raw = raw.strip()
    if raw == "":
        return current
    if raw.lower() == "clear":
        return None
    return raw


def run_wizard(*, env_path: Path | None = None) -> int:
    """Interactive setup. Returns exit code (0 = success, 1 = aborted)."""
    target = env_path or (paths.config_dir / ".env")
    existing = _parse_env_file(target)

    print("MIRA setup")
    print(f"  config file: {target}")
    print(
        "  Leave any prompt blank to keep the existing value. Type 'clear' "
        "to remove one. Ctrl-C to abort without writing."
    )

    updated = dict(existing)
    try:
        for prompt in _PROMPTS:
            new_val = _prompt_one(prompt, existing.get(prompt.env_name))
            if new_val is None:
                updated.pop(prompt.env_name, None)
            else:
                updated[prompt.env_name] = new_val
    except (KeyboardInterrupt, EOFError):
        print("\nAborted. No changes written.")
        return 1

    # Only rewrite if something actually changed — avoids bumping mtime and
    # churning file-watcher reloads for a purely informational re-run.
    if updated == existing:
        print("\nNo changes. .env left as-is.")
        return 0

    _write_env_file(target, updated)
    print(f"\nWrote {target} (chmod 600).")

    missing = [p.env_name for p in _PROMPTS if p.required and not updated.get(p.env_name)]
    if missing:
        print(
            "  Still missing required keys: " + ", ".join(missing) + "\n"
            "  Run `mira setup` again or edit the file directly."
        )
        return 1
    print("  All required keys are set. Run `mira doctor` to verify.")
    return 0
