    @staticmethod
    def _escape(value: str) -> str:
        return (
            value.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;")
            .replace("'", "&apos;")
        )

    def _palette(self, *, color: str | None, title: str, seed_id: str) -> dict[str, str]:
        seed = (color or title or seed_id).lower().encode("utf-8")
        digest = hashlib.sha256(seed).hexdigest()
        hue = int(digest[:2], 16)
        if color:
            color_name = color.lower()
            if "black" in color_name:
                return {"primary": "#3b3734", "secondary": "#6f6862", "accent": "#bcaea2"}
            if "blue" in color_name:
                return {"primary": "#5676a6", "secondary": "#88a1c2", "accent": "#d8e0f0"}
            if "red" in color_name:
                return {"primary": "#9d514f", "secondary": "#c98881", "accent": "#efd2c9"}
            if "green" in color_name:
                return {"primary": "#6b8966", "secondary": "#97b18f", "accent": "#d6e3d3"}
