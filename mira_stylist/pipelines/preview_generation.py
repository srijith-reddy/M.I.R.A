    def _browser_image_asset(self, path: Path) -> dict[str, str | int | float] | None:
        if not path.exists():
            return None
        payload = path.read_bytes()
        inspected_mime, width, height = inspect_image_bytes(payload)
        guessed_mime = inspected_mime or mimetypes.guess_type(str(path))[0] or "application/octet-stream"
        if guessed_mime in {"image/heic", "image/heif"}:
            converted = self._ensure_browser_capture(path)
            if converted:
                path = converted
                payload = path.read_bytes()
                inspected_mime, width, height = inspect_image_bytes(payload)
                guessed_mime = inspected_mime or mimetypes.guess_type(str(path))[0] or "image/jpeg"
        if not guessed_mime.startswith("image/"):
            return None
        if guessed_mime not in {"image/png", "image/jpeg", "image/gif", "image/webp", "image/svg+xml"}:
            return None
        if guessed_mime == "image/svg+xml":
            encoded = quote(payload.decode("utf-8"), safe="/:;,+?=&()#%[]@!$'*")
            data_uri = f"data:{guessed_mime},{encoded}"
        else:
            data_uri = f"data:{guessed_mime};base64,{base64.b64encode(payload).decode('ascii')}"
        return {
            "path": str(path),
            "mime": guessed_mime,
            "width": width or 768,
            "height": height or 1024,
            "data_uri": data_uri,
        }

    @staticmethod
    def _ensure_browser_capture(path: Path) -> Path | None:
        target = path.with_suffix(".preview.jpg")
        if target.exists():
            return target
        try:
            completed = subprocess.run(
                ["sips", "-s", "format", "jpeg", str(path), "--out", str(target)],
                check=False,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
            )
        except OSError:
            return None
        if completed.returncode != 0 or not target.exists():
            return None
        return target

