            "<rect x='38' y='40' width='692' height='1200' rx='28' fill='#fffaf5' stroke='#d8cab8'/>"
            "<text x='64' y='92' font-size='30' font-family='Arial' fill='#2f261f'>MIRA Stylist Preview</text>"
            f"<text x='64' y='126' font-size='18' font-family='Arial' fill='#66584d'>view={view_label} pose={pose_label} render={self._escape(request.render_mode.value)}</text>"
