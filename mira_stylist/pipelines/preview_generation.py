    def _load_vision_analysis(self, avatar: UserAvatar, *, view: str) -> dict | None:
        metadata_dir: Path | None = None
        if avatar.assets.body_profile_path:
            metadata_dir = Path(avatar.assets.body_profile_path).parent
        elif avatar.assets.metadata_path:
            metadata_dir = Path(avatar.assets.metadata_path).parent
        if metadata_dir is None:
            return None
        candidates = [metadata_dir / f"vision_{view}.json"]
        if view == "side":
            candidates.append(metadata_dir / "vision_front.json")
        for path in candidates:
            if not path.exists():
                continue
            try:
                payload = json.loads(path.read_text(encoding="utf-8"))
            except (OSError, json.JSONDecodeError):
                continue
            if payload.get("status") == "ok" and payload.get("points"):
                return payload
        return None

